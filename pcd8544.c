/* screen byte massive */
uint8_t screen[504];

/* cursor position */
uint8_t cursor_x;
uint8_t cursor_y;

static void write_cmd(uint8_t cmd) {
	PORTB &= ~(1 << PB2);  // CE chip enable - active low
	PORTD &= ~(1 << PD4);  // DC command (active low)
	// transmitting data
	SPDR = cmd;
	// wait till data transmit
	while (!(SPSR & (1 << SPIF)));
	PORTB |= (1 << PB2);   // CE chip disable - idle high
}

static void write_data(uint8_t data) {
	PORTB &= ~(1 << PB2);  // chip enable - active low
	PORTD |= 1 << PD4;     // data (active high)
	// transmitting data
	SPDR = data;
	// wait till data transmit
	while (!(SPSR & (1 << SPIF)));
	PORTB |= (1 << PB2);   // chip disable - idle high
}

void pcd8544_init(void) {
	/* Set pins as output */
	DDRB |= (1 << PB2); // SCE
	DDRB |= (1 << PB3); // DIN
	DDRB |= (1 << PB5); // CLK
	DDRD |= (1 << PD4); // D/C
	DDRD |= (1 << PD7); // RST

	// SPE  - SPI Enable
	// MSTR - Master device
	// SPR0 - Prescaler 16
	SPCR |= (1 << SPE) | (1 << MSTR) | (1 << SPR0);

	/* Reset display */
	PORTD |= (1 << PD7);  // reset high
	PORTB |= (1 << PB2);  // chip enable high
	_delay_ms(1);
	PORTD &= ~(1 << PD7); // reset low
	_delay_ms(1);
	PORTD |= (1 << PD7);  // reset high

	PORTB &= ~(1 << PB2);  /* Enable controller */
	write_cmd(0x21);  /* -LCD Extended Commands mode- */
	write_cmd(0x13);  /* LCD bias mode 1:48 */
	write_cmd(0x06);  /* Set temperature coefficient */
	write_cmd(0xC2);  /* Default VOP (3.06 + 66 * 0.06 = 7V) */
	write_cmd(0x20);  /* Standard Commands mode, Horizontal addressing mode */
	write_cmd(0x0C);  /* LCD in normal mode */
}

void pcd8544_clear(void) {
	cursor_x = 0;
	cursor_y = 0;
	memset(screen, 0, sizeof(screen));
}

void pcd8544_power(uint8_t on) {
	write_cmd(on ? 0x20 : 0x24);
}

void pcd8544_set_pixel(uint8_t x, uint8_t y, uint8_t value) {
	uint8_t *byte = &screen[y / 8 * 84 + x];
	if (value)
		*byte |= (1 << (y % 8));
	else
		*byte &= ~(1 << (y % 8));
}

void pcd8544_write_char(char code, uint8_t scale) {
	for (uint8_t x = 0; x < 5 * scale; x++)
		for (uint8_t y = 0; y < 7 * scale; y++)
			if (pgm_read_byte(&CHARSET[code - 32][x / scale]) & (1 << y / scale))
				pcd8544_set_pixel(cursor_x + x, cursor_y + y, 1);
			else
				pcd8544_set_pixel(cursor_x + x, cursor_y + y, 0);

	cursor_x += 5 * scale + 1;
	if (cursor_x >= 84) {
		cursor_x = 0;
		cursor_y += 7 * scale + 1;
	}
	if (cursor_y >= 48) {
		cursor_x = 0;
		cursor_y = 0;
	}
}

void pcd8544_write_string(char *str, uint8_t scale) {
	while(*str)
		pcd8544_write_char(*str++, scale);
}

void pcd8544_write_string_p(const char *str, uint8_t scale) {
	char c;
	while ((c = pgm_read_byte(str++)))
		pcd8544_write_char(c, scale);
}

void pcd8544_set_cursor(uint8_t x, uint8_t y) {
	cursor_x = x;
	cursor_y = y;
}

void pcd8544_render(void) {
	/* Set column and row to 0 */
	write_cmd(0x80);
	write_cmd(0x40);

	/* Write screen to display */
	for (uint16_t i = 0; i < sizeof(screen); i++)
		write_data(screen[i]);
}

void pcd8544_draw_hline(uint8_t x, uint8_t y, uint8_t length) {
	do {
		pcd8544_set_pixel(x++, y, 1);
	} while(--length);
}

void pcd8544_draw_vline(uint8_t x, uint8_t y, uint8_t length) {
	do {
		pcd8544_set_pixel(x, y++, 1);
	} while(--length);
}

void pcd8544_draw_line(uint8_t x1, uint8_t y1, uint8_t x2, uint8_t y2) {
	int8_t D;  // determinant
	int8_t trace_x = 1, trace_y = 1;  // steps
	// deltas
	int8_t delta_x = x2 - x1;
	int8_t delta_y = y2 - y1;
	// check if x2 > x1
	if (delta_x < 0) {
		delta_x = -delta_x;  // negate delta x
		trace_x = -trace_x;  // negate step x
	}
	// check if y2 > y1
	if (delta_y < 0) {
		delta_y = -delta_y;  // negate delta y
		trace_y = -trace_y;  // negate step y
	}
	// Bresenham condition for m < 1 (dy < dx)
	if (delta_y < delta_x) {
		// calculate determinant
		D = 2 * delta_y - delta_x;
		// draw first pixel
		pcd8544_set_pixel(x1, y1, 1);
		// check if x1 equal x2
		while (x1 != x2) {
			// update x1
			x1 += trace_x;
			// check if determinant is positive
			if (D >= 0) {
				y1 += trace_y;  // update y1
				D -= 2 * delta_x;  // update determinant
			}
			// update determinant
			D += 2 * delta_y;
			// draw next pixel
			pcd8544_set_pixel(x1, y1, 1);
		}
	// for m > 1 (dy > dx)
	} else {
		// calculate determinant
		D = delta_y - 2 * delta_x;
		// draw first pixel
		pcd8544_set_pixel(x1, y1, 1);
		// check if y2 equal y1
		while (y1 != y2) {
			// update y1
			y1 += trace_y;
			// check if determinant is positive
			if (D <= 0) {
				x1 += trace_x;  // update x1
				D += 2 * delta_y;  // update determinant
			}
			// update determinant
			D -= 2 * delta_x;
			// draw next pixel
			pcd8544_set_pixel(x1, y1, 1);
		}
	}
}
